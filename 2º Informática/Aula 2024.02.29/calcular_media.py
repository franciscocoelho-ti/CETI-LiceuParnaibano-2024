# Crie um algoritmo em Python que receba do usuário (aluno) 4 notas, e ao final exiba a média para o usuário.

# Entradas:
nota1 = float(input('Informe a 1ª nota: '))
nota2 = float(input('Informe a 2ª nota: '))
nota3 = float(input('Informe a 3ª nota: '))
nota4 = float(input('Informe a 4ª nota: '))


# Processamento --> Gerar a média
media = (nota1 + nota2 + nota3 + nota4) / 4


# Exibir a saída
print('A média dos notas foi: ', media)


